// TODO: Create a function to generate markdown content
function generateMarkdown(data) {
    return `
  # ${data.title}
  
  ## Description
  ${data.description}
  
  ## Table of Contents
  - [Installation](#installation)
  - [Usage](#usage)
  - [Contributing](#contributing)
  - [Tests](#tests)
  - [License](#license)
  - [Contact](#contact)
  
  ## Installation
  ${data.installation}
  
  ## Usage
  ${data.usage}
  
  ## Contributing
  ${data.contributing}
  
  ## Tests
  ${data.tests}
  
  ## License
  This project is licensed under the ${data.license} License.
  
  ## Contact
  For questions or feedback, you can reach me at ${data.email}.
  GitHub: [${data.username}](https://github.com/${data.username})
  `;
  }
  

init();
